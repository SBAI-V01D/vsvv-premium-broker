/**
 * Lookup postal code and return city/canton
 */
export function lookupPostalCode(plz) {
  if (!plz || plz.length !== 4 || !/^\d+$/.test(plz)) {
    return null;
  }
  
  const matches = SWISS_POSTAL_CODES.filter(entry => entry.plz === plz);
  
  if (matches.length === 0) {
    return null;
  }
  
  if (matches.length === 1) {
    return matches[0];
  }
  
  // Multiple matches - return all
  return matches;
}

/**
 * Check if postal code is valid (4 digits)
 */
export function isValidPostalCode(plz) {
  return plz && plz.length === 4 && /^\d+$/.test(plz);
}